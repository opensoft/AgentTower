/// Minimal [AppClient] stand-in. Each test overrides only the methods
/// the surface under test actually calls; everything else falls back to
/// `super`'s real implementation, which will throw if invoked (the
/// underlying [SocketClient] points at a path that will never bind).
/// That throw-on-misuse is intentional — it surfaces "this golden test
/// hit a method we forgot to fake" loudly rather than spinning forever.
class _FakeAppClient extends AppClient {
  _FakeAppClient({
    Map<String, dynamic>? dashboardResult,
    PagedResult? projectListResult,
    PagedResult? validationEntrypointListResult,
  })  : _dashboardResult = dashboardResult ?? Fixtures.dashboardResult(),
        _projectListResult = projectListResult ??
            const PagedResult(
              items: <Map<String, dynamic>>[],
              cursorNext: null,
              total: 0,
            ),
        _validationEntrypointListResult = validationEntrypointListResult ??
            const PagedResult(
              items: <Map<String, dynamic>>[],
              cursorNext: null,
              total: 0,
            ),
        super(
          session: DaemonSession(
            client: SocketClient('/nonexistent/never-bound.sock'),
          ),
        );

  final Map<String, dynamic> _dashboardResult;
  final PagedResult _projectListResult;
  final PagedResult _validationEntrypointListResult;

  @override
  Future<Map<String, dynamic>> dashboard({int recentLimit = 10}) async =>
      _dashboardResult;

  @override
  Future<PagedResult> projectList({String? cursorNext, int? limit}) async =>
      _projectListResult;

  @override
  Future<PagedResult> validationEntrypointList({
    String? cursorNext,
    int? limit,
    String? projectId,
    String? scopeKind,
    bool? enabled,
  }) async =>
      _validationEntrypointListResult;
}
