// =========================================================================
// Variant axis (3 themes × 2 densities = 6).
// =========================================================================

class _Variant {
  const _Variant({
    required this.theme,
    required this.density,
  });

  final enums.ThemeMode theme;
  final enums.DensityMode density;

  String get label => '${theme.wireValue} · ${density.wireValue}';

  /// Material's [m.ThemeMode]. `system` is mapped to the same
  /// brightness Light produces so the golden encodes a deterministic
  /// image (no host-OS dependency).
  m.ThemeMode get materialThemeMode => switch (theme) {
        enums.ThemeMode.light => m.ThemeMode.light,
        enums.ThemeMode.dark => m.ThemeMode.dark,
        enums.ThemeMode.system => m.ThemeMode.light,
      };

  /// Material's [m.VisualDensity] for the density axis. Comfortable maps
  /// to the platform default; Compact maps to the densest standard preset.
  m.VisualDensity get visualDensity => switch (density) {
        enums.DensityMode.comfortable => m.VisualDensity.standard,
        enums.DensityMode.compact => m.VisualDensity.compact,
      };

  Settings toSettings() => Settings(
        daemonSocketPath: '/var/run/agenttower/app.sock',
        theme: theme,
        density: density,
        notificationsGrouping: true,
        osNativeNotifications: false,
        debugLogging: false,
      );
}

const _variants = <_Variant>[
  _Variant(theme: enums.ThemeMode.light, density: enums.DensityMode.comfortable),
  _Variant(theme: enums.ThemeMode.light, density: enums.DensityMode.compact),
  _Variant(theme: enums.ThemeMode.dark, density: enums.DensityMode.comfortable),
  _Variant(theme: enums.ThemeMode.dark, density: enums.DensityMode.compact),
  _Variant(theme: enums.ThemeMode.system, density: enums.DensityMode.comfortable),
  _Variant(theme: enums.ThemeMode.system, density: enums.DensityMode.compact),
];

/// Wraps a workspace view in a [ProviderScope] + [m.MaterialApp] configured
/// for the given variant, so each scenario gets its own themed shell.
m.Widget _wrapped({
  required _Variant variant,
  required List<Override> overrides,
  required m.Widget child,
}) {
  final lightTheme = ColorTokens.light().copyWith(
    visualDensity: variant.visualDensity,
  );
  final darkTheme = ColorTokens.dark().copyWith(
    visualDensity: variant.visualDensity,
  );
  return ProviderScope(
    overrides: overrides,
    child: m.MaterialApp(
      debugShowCheckedModeBanner: false,
      theme: lightTheme,
      darkTheme: darkTheme,
      themeMode: variant.materialThemeMode,
      localizationsDelegates: const [
        ...baseLocalizationDelegates,
        AppLocalizations.delegate,
      ],
      supportedLocales: supportedLocales,
      home: m.Scaffold(body: child),
    ),
  );
}

